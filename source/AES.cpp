const uint8_t AES::Rcon[11] = {
  0x8d,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36
};

AES::AES() : state(16, 0) {}

AES::AES(const vector<uint8_t>& inp) : state(inp) 
{
    if (state.size() != 16)
        state.resize(16, 0);
}

void AES::setState(const vector<uint8_t>& inp) 
{
    state = inp;
    if (state.size() != 16)
        state.resize(16, 0);
}

vector<uint8_t> AES::getState() const 
{
    return state;
}

void AES::subByte() 
{
    for (int i = 0; i < 16; i++)
        state[i] = sbox[state[i]];
}

void AES::shiftRows() 
{
    vector<uint8_t> temp(16);
    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++)
            temp[r + 4 * c] = state[r + 4 * ((c + r) % 4)];
    state = temp;
}

uint8_t AES::xtime(uint8_t x) 
{
    return (uint8_t)((x << 1) ^ ((x & 0x80) ? 0x1B : 0x00));
}

void AES::mixColumns() 
{
    for (int c = 0; c < 4; c++) 
    {
        int i = 4 * c;
        uint8_t a0 = state[i], a1 = state[i+1], a2 = state[i+2], a3 = state[i+3];

        uint8_t r0 = xtime(a0) ^ xtime(a1) ^ a1 ^ a2 ^ a3;
        uint8_t r1 = a0 ^ xtime(a1) ^ xtime(a2) ^ a2 ^ a3;
        uint8_t r2 = a0 ^ a1 ^ xtime(a2) ^ xtime(a3) ^ a3;
        uint8_t r3 = xtime(a0) ^ a0 ^ a1 ^ a2 ^ xtime(a3);

        state[i]   = r0;
        state[i+1] = r1;
        state[i+2] = r2;
        state[i+3] = r3;
    }
}

void AES::addRoundKey(const vector<uint8_t>& roundKey) 
{
    for (int i = 0; i < 16; i++)
        state[i] ^= roundKey[i];
}

vector<vector<uint8_t>> AES::keyExpansion(const vector<uint8_t>& key) {
    vector<vector<uint8_t>> roundKeys(11, vector<uint8_t>(16));

    for (int i = 0; i < 16; i++)
        roundKeys[0][i] = key[i];

    for (int r = 1; r < 11; r++) {
        vector<uint8_t> prev = roundKeys[r - 1];
        vector<uint8_t> temp(4);

        temp[0] = sbox[prev[13]] ^ Rcon[r];
        temp[1] = sbox[prev[14]];
        temp[2] = sbox[prev[15]];
        temp[3] = sbox[prev[12]];

        for (int i = 0; i < 16; i++)
            roundKeys[r][i] = prev[i] ^ temp[i % 4];
    }

    return roundKeys;
}

void AES::invSubByte() 
{
    for (int i = 0; i < 16; i++)
        state[i] = inv_sbox[state[i]];
}

void AES::invShiftRows() 
{
    vector<uint8_t> temp(16);
    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++)
            temp[r + 4 * c] = state[r + 4 * ((c - r + 4) % 4)];
    state = temp;
}

uint8_t AES::mul(uint8_t a, uint8_t b) 
{
    uint8_t result = 0;
    while (b) 
    {
        if (b & 1)
            result ^= a;
        a = (uint8_t)((a << 1) ^ ((a & 0x80) ? 0x1B : 0x00));
        b >>= 1;
    }
    return result;
}

void AES::invMixColumns() 
{
    for (int c = 0; c < 4; c++) 
    {
        int i = 4 * c;
        uint8_t a0 = state[i], a1 = state[i+1], a2 = state[i+2], a3 = state[i+3];

        state[i]   = mul(a0,0x0e) ^ mul(a1,0x0b) ^ mul(a2,0x0d) ^ mul(a3,0x09);
        state[i+1] = mul(a0,0x09) ^ mul(a1,0x0e) ^ mul(a2,0x0b) ^ mul(a3,0x0d);
        state[i+2] = mul(a0,0x0d) ^ mul(a1,0x09) ^ mul(a2,0x0e) ^ mul(a3,0x0b);
        state[i+3] = mul(a0,0x0b) ^ mul(a1,0x0d) ^ mul(a2,0x09) ^ mul(a3,0x0e);
    }
}


vector<uint8_t> AES::encryptBlock(const vector<uint8_t>& block, const vector<uint8_t>& key) 
{
    setState(block);
    auto roundKeys = keyExpansion(key);

    addRoundKey(roundKeys[0]);
    for (int r = 1; r <= 9; r++) 
    {
        subByte();
        shiftRows();
        mixColumns();
        addRoundKey(roundKeys[r]);
    }

    subByte();
    shiftRows();
    addRoundKey(roundKeys[10]);

    return state;
}

vector<uint8_t> AES::decryptBlock(const vector<uint8_t>& block, const vector<uint8_t>& key) 
{
    setState(block);
    auto roundKeys = keyExpansion(key);

    addRoundKey(roundKeys[10]);
    for (int r = 9; r >= 1; r--) 
    {
        invShiftRows();
        invSubByte();
        addRoundKey(roundKeys[r]);
        invMixColumns();
    }

    invShiftRows();
    invSubByte();
    addRoundKey(roundKeys[0]);

    return state;
}

void AES::encrypt(const vector<uint8_t>& key) 
{
    auto r = keyExpansion(key);
    addRoundKey(r[0]);
    for (int i = 1; i <= 9; i++) 
    {
        subByte();
        shiftRows();
        mixColumns();
        addRoundKey(r[i]);
    }
    subByte();
    shiftRows();
    addRoundKey(r[10]);
}

void AES::decrypt(const vector<uint8_t>& key) 
{
    auto r = keyExpansion(key);
    addRoundKey(r[10]);
    for (int i = 9; i >= 1; i--) 
    {
        invShiftRows();
        invSubByte();
        addRoundKey(r[i]);
        invMixColumns();
    }
    invShiftRows();
    invSubByte();
    addRoundKey(r[0]);
}
